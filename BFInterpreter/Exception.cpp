#include "Exception.h"

///////////////////////////////////////////////////////////////////////////
Exception::Exception()
{
	// nothing to do
}

///////////////////////////////////////////////////////////////////////////
Exception::Exception(const std::string & message) :
m_message(message)
{
	// nothing to do
}

///////////////////////////////////////////////////////////////////////////
const char * Exception::what() const
{
	return m_message.c_str();
}

///////////////////////////////////////////////////////////////////////////
const std::string & Exception::getMessage() const
{
	return m_message;
}

///////////////////////////////////////////////////////////////////////////
void Exception::setMessage(const std::string & message)
{
	m_message = message;
}